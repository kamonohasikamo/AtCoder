#include <stdio.h>

int main() {
	//---------------------
	// N
	// a[0], a[1],..,a[n-1]
	//---------------------
	int n;
	scanf("%d", n);
	int a[n];
	int i;
	for (i=0;i<n;i++) {
		scanf("%d", &a[i]);
	}

	//---------------------
	// H, W
	// a[0][0], a[0][1], ..., a[0][W-1]
	// a[1][0], a[1][1], ..., a[1][W-1]
	// .......
	// a[H-1][0], a[H-1][1], ..., a[H-1][W-1]
	//---------------------
	int H, W;
	scanf("%d %d", &H, &W);
	int a[H][W];
	int i;
	for (i=0;i<H;i++) {
		scanf("%s", a[i]);
	}

	//---------------------
	// N
	// t[0] x[0] y[0]
	// ..
	// t[n-1] x[n-1] y[n-1]
	//---------------------
	scanf("%d",&n);
	int t[n],x[n],y[n];
	for(i=0;i<n;i++){
		scanf("%d %d %d",&t[i], &x[i],&y[i]);
	}

	//------------------------
	// 文字列S
	//------------------------
	char s[256];
	scanf("%s", s);

	//----------------------------------
	// 個数N、文字列の長さL、文字列S[i]
	// S[0]
	// ..
	// S[N-1]
	//----------------------------------
	int l;
	scanf("%d%d", &n, &l);
	char s[n][l];
	for (i = 0; i < n; i++) {
		scanf("%s", s[i]);
	}
}
